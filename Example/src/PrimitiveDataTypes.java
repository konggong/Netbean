public class PrimitiveDataTypes {
    public static void main(String[] args) {
        int numInt ; // 2,147,483,647;
        numInt = 2147483647;
        System.out.println("num = " + numInt);
        
        numInt = -2147483647;
        System.out.println("num = " + numInt);
        
        long numLong; //9,223,372,036,854,775,807
        numLong =  -9223372036854775808L;
        System.out.println("numLong = " + numLong);
        
        numLong = 9223372036854775807L;
        System.out.println("numLong = " + numLong);
        
    }
}
